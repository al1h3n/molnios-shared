#!/usr/bin/env bash
# molnios-templates.sh - Template generator for MolniOS color system
# Part of the MolniOS project.
# ==============================================================================
# Generates application-specific config files from pywal color cache.
# Works with any backend (pywal16, wallust, matugen) as long as colors are
# available in ~/.cache/wal/colors.sh
# ==============================================================================

set -euo pipefail

CACHE_DIR="${XDG_CACHE_HOME:-$HOME/.cache}"
CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}"

# Source colors from cache (pywal-compatible format)
COLORS_SH="$CACHE_DIR/wal/colors.sh"
[[ -f "$COLORS_SH" ]] || {
    echo "Error: colors.sh not found at $COLORS_SH" >&2
    exit 1
}

# shellcheck disable=SC1090
source "$COLORS_SH"

# Fallback if colors are in different variables
color0="${color0:-$wal_color0}"
color1="${color1:-$wal_color1}"
color2="${color2:-$wal_color2}"
color3="${color3:-$wal_color3}"
color4="${color4:-$wal_color4}"
color5="${color5:-$wal_color5}"
color6="${color6:-$wal_color6}"
color7="${color7:-$wal_color7}"
color8="${color8:-$wal_color8}"
color9="${color9:-$wal_color9}"
color10="${color10:-$wal_color10}"
color11="${color11:-$wal_color11}"
color12="${color12:-$wal_color12}"
color13="${color13:-$wal_color13}"
color14="${color14:-$wal_color14}"
color15="${color15:-$wal_color15}"

background="${background:-$color0}"
foreground="${foreground:-$color7}"
cursor="${cursor:-$color7}"

# Helper: convert hex to rgba
hex_to_rgba(){
    local hex="$1"
    local alpha="${2:-FF}"
    hex="${hex#\#}"
    echo "rgba(${hex}${alpha})"
}

# Helper: ensure directory exists
ensure_dir(){
    mkdir -p "$(dirname "$1")"
}

