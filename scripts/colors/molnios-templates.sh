# ══════════════════════════════════════════════════════════════════════════════
# DUNST THEME
# ══════════════════════════════════════════════════════════════════════════════

generate_dunst(){
    local out="$CONFIG_DIR/dunst/dunstrc-dynamic"
    ensure_dir "$out"

    # Calculate transparency versions
    local bg_trans="${color0}1A"  # 10% opacity
    local fg_trans="${foreground}BF"  # 75% opacity

    cat > "$out" << EOF
# MolniOS Dunst Theme - Dynamic Colors
# Generated by molnios-templates.sh
# DO NOT EDIT - This file is automatically generated
# Include this in your main dunstrc with: include = ~/.config/dunst/dunstrc-dynamic

[urgency_low]
background = "${bg_trans}"
foreground = "${fg_trans}"
frame_color = "${color4}"
timeout = 5

[urgency_normal]
background = "${bg_trans}"
foreground = "${foreground}"
frame_color = "${color4}"
timeout = 10

[urgency_critical]
background = "${color1}80"
foreground = "${foreground}"
frame_color = "${color1}"
timeout = 0
EOF
}

