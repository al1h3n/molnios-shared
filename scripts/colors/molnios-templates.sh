# ══════════════════════════════════════════════════════════════════════════════
# GTK CSS (for libadwaita apps)
# ══════════════════════════════════════════════════════════════════════════════

generate_gtk(){
    local gtk3_out="$CONFIG_DIR/gtk-3.0/colors-dynamic.css"
    local gtk4_out="$CONFIG_DIR/gtk-4.0/colors-dynamic.css"

    ensure_dir "$gtk3_out"
    ensure_dir "$gtk4_out"

    local content
    content=$(cat << EOF
/* MolniOS GTK Theme - Dynamic Colors */
/* Generated by molnios-templates.sh */
/* DO NOT EDIT - This file is automatically generated */

@define-color theme_bg_color ${background};
@define-color theme_fg_color ${foreground};
@define-color theme_base_color ${color0};
@define-color theme_text_color ${foreground};
@define-color theme_selected_bg_color ${color4};
@define-color theme_selected_fg_color ${background};

@define-color accent_color ${color4};
@define-color accent_bg_color ${color4};
@define-color accent_fg_color ${background};

@define-color destructive_color ${color1};
@define-color destructive_bg_color ${color1};
@define-color destructive_fg_color ${background};

@define-color success_color ${color2};
@define-color success_bg_color ${color2};
@define-color success_fg_color ${background};

@define-color warning_color ${color3};
@define-color warning_bg_color ${color3};
@define-color warning_fg_color ${background};

@define-color error_color ${color1};
@define-color error_bg_color ${color1};
@define-color error_fg_color ${background};

@define-color window_bg_color ${background};
@define-color window_fg_color ${foreground};
@define-color view_bg_color ${color0};
@define-color view_fg_color ${foreground};

@define-color headerbar_bg_color ${color1};
@define-color headerbar_fg_color ${foreground};
@define-color headerbar_backdrop_color ${color0};

@define-color popover_bg_color ${color1};
@define-color popover_fg_color ${foreground};

@define-color card_bg_color ${color1};
@define-color card_fg_color ${foreground};

@define-color dialog_bg_color ${color1};
@define-color dialog_fg_color ${foreground};
EOF
)

    echo "$content" > "$gtk3_out"
    echo "$content" > "$gtk4_out"
}

# ══════════════════════════════════════════════════════════════════════════════
# ZSH/FISH color exports
# ══════════════════════════════════════════════════════════════════════════════

generate_shell_exports(){
    # These are already generated by pywal/wallust/matugen
    # We just ensure they exist in the expected location

    if [[ -f "$CACHE_DIR/wal/colors.sh" ]];then
        # Already generated by backend
        :
    fi

    if [[ -f "$CACHE_DIR/wal/colors.fish" ]];then
        # Already generated by backend
        :
    fi
}

# ══════════════════════════════════════════════════════════════════════════════
# Execute all generators
# ══════════════════════════════════════════════════════════════════════════════

generate_waybar
generate_rofi
generate_dunst
generate_swaync
generate_kitty
generate_gtk
generate_shell_exports

exit 0
